'use client'

import type { ComponentProps } from 'react'

import { cn } from '@a/ui'
import { Collapsible, CollapsibleContent, CollapsibleTrigger } from '@a/ui/collapsible'
import { BookIcon, ChevronDownIcon } from 'lucide-react'

export type SourcesProps = ComponentProps<'div'>

export const Sources = ({ className, ...props }: SourcesProps) => (
  <Collapsible className={cn('not-prose mb-4 text-xs text-primary', className)} {...props} />
)

export type SourcesTriggerProps = ComponentProps<typeof CollapsibleTrigger> & {
  count: number
}

export const SourcesTrigger = ({ children, className, count, ...props }: SourcesTriggerProps) => (
  <CollapsibleTrigger className={cn('flex items-center gap-2', className)} {...props}>
    {children ?? (
      <>
        <p className='font-medium'>Used {count} sources</p>
        <ChevronDownIcon className='size-4' />
      </>
    )}
  </CollapsibleTrigger>
)

export type SourcesContentProps = ComponentProps<typeof CollapsibleContent>

export const SourcesContent = ({ className, ...props }: SourcesContentProps) => (
  <CollapsibleContent
    className={cn(
      'mt-3 flex w-fit flex-col gap-2',
      'outline-none data-[state=closed]:animate-out data-[state=closed]:fade-out-0 data-[state=closed]:slide-out-to-top-2 data-[state=open]:animate-in data-[state=open]:slide-in-from-top-2',
      className
    )}
    {...props}
  />
)

export type SourceProps = ComponentProps<'a'>

export const Source = ({ children, href, title, ...props }: SourceProps) => (
  // oxlint-disable-next-line nextjs/no-html-link-for-pages
  <a className='flex items-center gap-2' href={href} rel='noreferrer' target='_blank' {...props}>
    {children ?? (
      <>
        <BookIcon className='size-4' />
        <span className='block font-medium'>{title}</span>
      </>
    )}
  </a>
)
